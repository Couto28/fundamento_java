public class loopsDoWhile {

    public static void main(String[] Args){

        // do while , ele excuta o trexo de código ao menos uma vez , mesmo que seja falso ele será excutado .

        int y = 15;

        do {
            System.out.println(" Executando código . . . !");
            y++;
        } while (y > 16); // Se o código for verdadeiro ele será excutado variás vezes .
                          // se for falso ele excuta apenas uma vez .

    }
}
